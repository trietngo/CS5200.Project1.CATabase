CREATE TABLE IF NOT EXISTS "Cats" (
	"cat_id"	INTEGER,
	"cat_name"	TEXT,
	"cat_birthday"	TEXT,
	"cat_weight_lb"	REAL
);
CREATE TABLE IF NOT EXISTS "CatBreed" (
	"cat_id"	INTEGER,
	"breed_id"	INTEGER,
	FOREIGN KEY("cat_id") REFERENCES "Cats"("cat_id"),
	FOREIGN KEY("breed_id") REFERENCES "Breeds"("breed_id")
